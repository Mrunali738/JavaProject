package samplePackage;

public class SwitchCondition {
	public static void main(String[] args) {
		String Day = "Monday";
		switch (Day) {
		
		case "Sunday":
			System.out.println("Sunday");
			break;
			
		case "Monday":
			System.out.println("Monday");
			break;
			
		case "Tuesday":
			System.out.println("Tuesday");
			break;
			
			default:
				System.out.println("Out of Day");
			break;
		}
		}
	}

