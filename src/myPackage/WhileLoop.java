package myPackage;

public class WhileLoop {

	public static void main(String[] args) {
		// This is while loop example
		int x = 1;
		while(x<10) {   
			System.out.println(x);
			x++;
		}
		//This is do while loop example
		int z = 10;
		do {
			System.out.println(z);
			z--;
		}
		while(z>1);
	}

}
